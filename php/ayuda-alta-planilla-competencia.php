		<h2>Ayuda - Planillas de Saltos</h2>	
		<p>
			En esta página se definen los saltos a realizar por parte del clavadista en cada una de las pruebas en que participa. El sistema trae el nombre del entrenador y del clavadista suministrado en la Inscripción del clavadista, tiene la opción de registrar a un segundo clavadista cuando se trata de una planilla de saltos sincronizados. 
		</p>
		<br>
		<p>
			Debes definir la categoría en que participa el clavadista y la modalidad a competir, esta información es validada contra las pruenas registradas y convocadas para la competencia.
		</p>
		<br>
		<p>
			En la parte inferior, se registra la lista de saltos, la primera columna tiene el número que indica el orden de los saltos, sobre este número se puede dar click para consultar un salto por su nombre o código y lo puedes seleccionar para incluirlo en la serie de saltos. Luego viene la columna código, allí debes escribir el código del salto, en caso de que no conozcas el código, usa la opción ya descrita del click sobre el número para buscarlo. Luego debes colocar la posición en que se ejecuta el salto, puedes escoger o escribir directamente la posición (A, B, C ó D). 
		</p>
		<br>
		<p>
			La altura: en caso de las modalidades de 1 metro o de 3 metros no es necesario colocar la altura de ejecución del salto, en los otros casos, se debe seleccionar o escribir directamente la altura. 
		</p>
		<br>
		<p>
			Abierto: si se trata de una pruena donde el clavadista es de la categoría juvenil y a la vez va a participar en la categoría abierta, puede marcar los saltos que se deben tener en cuenta para la categoría abierta.
		</p>
		<br>
		<p>
			Finalmente, al darle click en el botón "Verificar" el sistema valida la información y trae el nombre y el grado de dificultad de cada salto y si la información es correcta, se habilitan los botones "Modificar" por si quieres cambiar algún dato de la planilla y el botón "Guardar" para registrar la planilla en el sistema.
		</p>
	</div>
</section>
